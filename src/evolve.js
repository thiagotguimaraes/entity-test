function loadDataset() {
    var outputs = [];
    var inputs = [];
    dataset.split(';').forEach(x => {
        var columns = x.split(',');
        var output = columns[0].trim();
        var desiredOutput;
        if(output == 'B') {
            desiredOutput = [0, 1, 0];
        } else if (output == 'L') {
            desiredOutput = [1, 0, 0];
        } else if (output == 'R'){
            desiredOutput = [0, 0, 1];
        }
        outputs.push(desiredOutput);
        inputs.push(columns.slice(1));
    });
    var loadedDataset = [];
    outputs.forEach((x,i) => {
        loadedDataset.push({
            input: inputs[i],
            output: outputs[i]
        })
    })
    return loadedDataset;
}

